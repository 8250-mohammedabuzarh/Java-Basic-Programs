package javabasics;

public class Datatypevalues {
    int a;
    char b;
    double c;
    boolean d;
    short e;
    long f;
    float g;
    public static void main(String[] abs) {
        Datatypevalues obj = new Datatypevalues();
        System.out.println("Default values of data types:");
        System.out.println("int: " + obj.a);
        System.out.println("char: " + obj.b);
        System.out.println("double: " + obj.c);
        System.out.println("boolean: " + obj.d);
        System.out.println("short: " + obj.e);
        System.out.println("long: " + obj.f);
        System.out.println("float: " + obj.g);
    }

}
